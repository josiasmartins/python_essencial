"""
2. Faça um programa que peça para o usuário digitar três valores inteiros e imprima a soma deles.
"""

numero1: int = int(input("Digite o primeiro valor: "))
numero2: int = int(input("Digite o segundo valor: "))
numero3: int = int(input("Digite o terceiro valor: "))

soma: int = numero1 + numero2 + numero3

print(f"A soma dos números {numero1} com {numero2} e {numero3} é {soma}")

